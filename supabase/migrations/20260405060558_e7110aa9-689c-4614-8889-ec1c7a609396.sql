Love addiction and codependency are patterns where relationships become a source of compulsive behavior rather than genuine connection. **An estimated 3-6% of the population meets criteria for love addiction**, though many more exhibit subclinical patterns that quietly shape their relationship choices.

### What Is Love Addiction?

Love addiction involves:
- Obsessive focus on a romantic interest
- Confusing intensity with intimacy
- Using relationships to avoid facing yourself
- Withdrawal symptoms when alone (anxiety, depression, panic)
- Repeatedly choosing unavailable or harmful partners
- An inability to leave toxic relationships

Pia Mellody''s groundbreaking work identifies love addiction as rooted in childhood attachment wounds — specifically, **the experience of being valued for what you do rather than who you are**. This creates a deep-seated belief that love must be earned through performance, sacrifice, or tolerating mistreatment.

In ENM and polyamorous contexts, love addiction can manifest in unique ways. Someone might pursue new connections compulsively — not because they genuinely want multiple relationships, but because the "new relationship energy" (NRE) high temporarily fills an inner void. The structure of ethical non-monogamy can inadvertently enable addictive patterns when the motivation is escapism rather than authentic connection.

### Codependency: The Other Side

Codependency involves:
- Excessive caretaking at the expense of your own needs
- Poor boundaries — difficulty saying no
- Self-worth dependent on being needed
- Controlling behavior disguised as helpfulness
- Suppressing your own emotions to manage others''
- Taking responsibility for others'' feelings and choices

**Melody Beattie''s research found that codependency affects an estimated 96 million Americans.** In polyamorous relationships, codependency can look like over-functioning as a "hinge" partner — managing everyone''s emotions, scheduling, and comfort while neglecting your own needs entirely. You might find yourself saying yes to relationship configurations that don''t serve you because you fear disappointing a partner.

### The Love Addict/Love Avoidant Dance

Often, love addicts pair with love avoidants in a toxic cycle:
1. Love addict pursues → Love avoidant retreats
2. Love avoidant creates distance → Love addict escalates
3. Love addict gives up → Love avoidant re-engages
4. Cycle repeats with increasing intensity

This dynamic can play out across multiple relationships simultaneously in polyamorous contexts, with different partners occupying different roles in the cycle. Recognizing this pattern is the first step toward breaking it.

### Reflection

Consider: Have you ever stayed in a relationship primarily because being alone felt unbearable? What feelings come up when you imagine a weekend with no romantic plans? These questions can reveal whether your relationship patterns are driven by genuine desire for connection or by avoidance of being with yourself.

