Compulsive relationship patterns often feel normal because they''re all we''ve known. Recognizing them requires honest self-examination and an understanding of how the brain creates addictive loops.

### The Neurochemistry of Love Addiction

Falling in love triggers a neurochemical cocktail similar to cocaine:
- **Dopamine**: Reward and craving
- **Norepinephrine**: Excitement and obsession
- **Serotonin**: Drops, causing intrusive thoughts (similar to OCD)
- **Oxytocin**: Bonding and attachment

For love addicts, the brain becomes dependent on these chemicals, requiring increasingly intense experiences to achieve the same "high." **Research by Helen Fisher at Rutgers found that the brains of rejected lovers show activation patterns identical to those of drug addicts in withdrawal.**

Understanding this neurochemistry is empowering, not pathologizing. It explains why "just stop" doesn''t work — your brain is literally wired to seek the next fix. In polyamorous contexts, this can manifest as compulsively seeking new partners during the dopamine-rich NRE phase while neglecting the deeper, quieter rewards of established relationships.

### Red Flags: Compulsive Relationship Patterns

- **Serial monogamy**: Jumping from relationship to relationship without processing the previous one
- **Instant intimacy**: Sharing everything immediately, moving fast past healthy pacing
- **Fantasy bonding**: Building elaborate fantasies about someone you barely know
- **Stalking behaviors**: Checking social media compulsively, driving by their house
- **Sexual compulsivity**: Using sex to manage emotions or fill emptiness
- **Caretaking compulsions**: Finding people to "fix" or "save"
- **Relationship hoarding**: Accumulating partners without the emotional capacity to truly engage with any of them

### Distinguishing Healthy Desire from Compulsion

Not all intense feelings are addictive. Healthy relationship energy includes excitement tempered by groundedness, desire accompanied by respect for boundaries, and interest that coexists with a full life outside the relationship. The key difference: **healthy desire enhances your life; compulsive patterns consume it.**

Ask yourself: Can I enjoy the feeling without acting on it immediately? Can I tolerate uncertainty about where this is going? Do I still maintain my friendships, hobbies, and self-care routines?

### Self-Assessment Questions

1. Do I feel complete only when in a relationship?
2. Do I repeatedly choose partners who are emotionally unavailable?
3. Do I confuse sexual attraction with love?
4. Do I stay in relationships long after they''ve become harmful?
5. Do I use relationships to avoid dealing with my own issues?
6. Do I feel panicked or empty when a partner needs space?
7. Do I make major life decisions based on keeping a partner happy?

If you answered yes to 3 or more, compulsive patterns may be at play. This isn''t a diagnosis — it''s an invitation to explore these patterns with curiosity and compassion, ideally with a therapist who understands both attachment and relationship diversity.

