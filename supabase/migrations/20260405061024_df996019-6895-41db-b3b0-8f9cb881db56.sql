Mental health first aid means knowing how to recognize when someone is in distress and responding appropriately — not as a therapist, but as a caring partner or friend. The goal is to provide initial support and connect the person with professional help when needed.

### Levels of Mental Health Distress

Understanding the spectrum of distress helps you calibrate your response:

**Level 1 — Everyday stress**: Normal fluctuations in mood, temporary frustration, situational sadness. Response: Empathetic listening, validation, normalizing.

**Level 2 — Increased distress**: Sleep disruption lasting more than a week, social withdrawal, persistent irritability, loss of interest in usual activities, increased substance use. Response: Express concern, offer support, gently suggest professional help.

**Level 3 — Acute crisis**: Panic attacks, dissociative episodes, severe emotional dysregulation, self-harm urges, inability to function in daily life. Response: Stay calm, ensure safety, help access professional support, don''t leave them alone if they''re at risk.

**Level 4 — Emergency**: Active suicidal intent with a plan, psychotic episode, severe self-harm, danger to self or others. Response: Call 988 (Suicide & Crisis Lifeline), go to the nearest ER, call 911 if immediate danger.

### Warning Signs That Require Attention

**Behavioral changes**:
- Dramatic shifts in sleep patterns (sleeping all day or not at all)
- Giving away possessions or "getting affairs in order"
- Sudden calmness after a period of severe depression (this can indicate a decision to attempt suicide)
- Increased risk-taking or recklessness
- Withdrawing from all social contact, including partners they normally seek out
- Stops responding to messages entirely when they normally communicate regularly

**Verbal cues**:
- "I can''t do this anymore" (explore what "this" means — it might be a situation, not life itself)
- "Everyone would be better off without me"
- "I don''t see the point"
- "I just want the pain to stop"
- Jokes about death or suicide that feel different from their normal humor

**Emotional indicators**:
- Hopelessness that persists across conversations and contexts
- Extreme mood swings that are out of character
- Emotional numbness or flat affect when they''re usually expressive
- Intense shame or guilt that seems disproportionate to the situation

### Special Considerations for ENM/Poly Relationships

In non-monogamous relationships, crisis signs may be distributed across partners. One partner might see the sleep changes, another notices the withdrawal, and a third picks up on the verbal cues. **Communication between metamours about a shared partner''s well-being — with appropriate consent and boundaries — can be lifesaving.**

However, be careful about:
- Ganging up on someone about their mental health (feels like an intervention, not support)
- Sharing information between partners without the person''s consent (unless there''s immediate danger)
- Assuming one partner is "responsible" for monitoring the person''s mental health

### The ALGEE Framework

Mental Health First Aid International developed the ALGEE framework:
- **A**pproach, assess for risk of suicide or harm
- **L**isten nonjudgmentally
- **G**ive reassurance and information
- **E**ncourage appropriate professional help
- **E**ncourage self-help and other support strategies

### Reflection

Have you ever noticed warning signs in a partner and been unsure how to respond? What would have been helpful to know in that moment? Preparing yourself now — before a crisis — means you''ll respond more effectively when it matters most.'
