Money is the number one source of conflict in romantic relationships — more than sex, parenting, or household responsibilities. **Research from the Institute for Divorce Financial Analysts found that financial issues contribute to 22% of all divorces**, making it the third leading cause after basic incompatibility and infidelity. Yet most couples never have a truly honest conversation about money until they''re already in crisis.

### The Emotional Weight of Money

Money is never just about money. It carries deep emotional meaning shaped by our upbringing, culture, class background, and personal history. Money represents:

- **Security**: For those who grew up with financial instability, money represents survival itself
- **Freedom**: The ability to make choices without constraint
- **Power**: Who earns more often translates (consciously or not) into who has more say
- **Love**: Some people express care through financial generosity; others feel controlled by it
- **Worth**: In a capitalist society, net worth and self-worth become dangerously entangled

### Money Scripts

Financial psychologists Brad and Ted Klontz identified four common "money scripts" — unconscious beliefs that drive financial behavior:

1. **Money avoidance**: "Money is bad" or "I don''t deserve money." Leads to financial neglect, underearning, and giving money away to the point of self-harm.
2. **Money worship**: "More money will solve everything." Leads to workaholism, overspending, and never feeling "enough."
3. **Money status**: "My worth equals my net worth." Leads to competitive spending, secrecy about debt, and shame about financial reality.
4. **Money vigilance**: "You must save for a rainy day." Can lead to healthy saving but also to excessive anxiety, deprivation, and difficulty enjoying money.

Understanding your own money script — and your partner(s)'' scripts — is the first step toward financial intimacy.

### Money in ENM & Polyamorous Relationships

Financial dynamics become exponentially more complex with multiple partners:
- **Unequal earning power** across a polycule can create resentment or dependency
- **Date night costs** multiply — who pays for what, and how do you budget for multiple relationships?
- **Cohabitation decisions** involve complex financial entanglements with potentially more than two people
- **Gift-giving norms** may differ between partners, creating perceived favoritism
- **Financial crises** affect the entire relationship network, not just one partnership
- **Legal protections** like marriage, joint accounts, and beneficiary status aren''t equally available to all partners

### The Cost of Financial Secrecy

**A National Endowment for Financial Education survey found that 43% of adults with combined finances admitted to financial deception** — hiding purchases, accounts, debt, or income. In polyamorous relationships, financial secrecy can compound. A partner might hide spending on one relationship from another partner, creating cascading trust violations.

