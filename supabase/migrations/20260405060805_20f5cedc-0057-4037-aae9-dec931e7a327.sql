**Non-consensual sharing of intimate images (NCII)** — commonly called "revenge porn" — is illegal in the majority of US states and many countries worldwide. Penalties can include criminal charges, fines, and civil liability. Even threatening to share intimate images is a criminal offense in many jurisdictions.

If you''re a victim of NCII:
- Document everything (screenshots of threats, distribution evidence)
- Report to the platform for immediate takedown
- Contact the Cyber Civil Rights Initiative (cybercivilrights.org)
- Consider filing a police report
- Know that this is NOT your fault, regardless of the circumstances of the original sharing

