Digital spaces should be places of connection and expression, but for many people — especially women, LGBTQ+ individuals, and people of color — they can become sites of harassment, surveillance, and abuse. Understanding these dynamics is essential for both protecting yourself and being an ethical digital citizen.

### Forms of Digital Harassment

Digital harassment exists on a spectrum:

- **Unwanted contact**: Repeated messages after being asked to stop, creating new accounts to circumvent blocks
- **Cyberstalking**: Monitoring someone''s online activity, tracking their location through apps, showing up at places based on social media posts
- **Doxing**: Publishing someone''s private information (home address, workplace, phone number) without consent
- **Outing**: Revealing someone''s sexual orientation, gender identity, relationship structure, or kink involvement without permission
- **Image-based abuse**: Sharing, threatening to share, or creating (deepfake) intimate images without consent
- **Coordinated harassment**: Mobilizing others to target an individual through mass reporting, review bombing, or pile-ons
- **Sextortion**: Threatening to share intimate content unless the victim pays money or provides additional content

### Who Is Most at Risk?

**Research by the Pew Research Center** found that 41% of Americans have experienced online harassment, with women and LGBTQ+ individuals disproportionately targeted. In ENM and polyamorous communities, the risk of outing adds an additional dimension — someone might threaten to reveal your relationship structure to your employer, family, or custody-related proceedings.

Trans and non-binary individuals face particularly acute risks, including deadnaming, targeted misgendering campaigns, and harassment designed to force them off platforms entirely.

