For neurodivergent individuals, sensory processing differences profoundly affect physical intimacy, affection, and shared physical spaces. Understanding and accommodating these differences is not a compromise — it''s a foundation for genuine connection.

### How Sensory Processing Differs

Sensory processing exists on a spectrum with two key dimensions:

**Hypersensitivity (over-responsive)**: Experiencing sensory input more intensely than neurotypical people.
- Light touch may feel irritating, ticklish, or even painful
- Certain fabrics or textures are unbearable against the skin
- Strong scents (perfume, cologne, scented candles) may be overwhelming or nauseating
- Sounds that others barely notice (clock ticking, refrigerator hum, breathing) may be distractingly loud
- Bright or flickering lights can cause headaches, anxiety, or sensory overwhelm

**Hyposensitivity (under-responsive)**: Needing more intense sensory input to register.
- Preferring deep pressure over light touch
- Not noticing temperature changes that others find uncomfortable
- Seeking strong flavors, loud music, or intense physical sensations
- May not notice pain or discomfort signals until they''re severe

**Sensory seeking**: Actively craving certain types of sensory input. This might look like needing weighted blankets, fidget tools, specific textures, or rhythmic movement to feel regulated.

### Impact on Physical Intimacy

Sensory differences create unique considerations for physical intimacy:

**Touch**: Some neurodivergent people find light, feathery touch uncomfortable but deeply enjoy firm pressure. Others may have specific areas of their body that are hypersensitive. The "romantic" touches depicted in media — gentle stroking, light finger-trailing — may be actively unpleasant.

**Environment**: Sensory-safe intimacy requires attention to:
- Lighting (dim, natural, or colored light instead of harsh fluorescents)
- Sound (background noise preferences — some need silence, others need white noise or music)
- Temperature (the room, the sheets, body temperature differences)
- Scent (unscented products may be necessary; avoid surprising partners with new perfumes)
- Texture (bedding, clothing, lubricants — all have sensory dimensions)

**Timing**: Sensory capacity fluctuates. A neurodivergent person who welcomed touch in the morning may be completely overloaded by evening. This isn''t rejection — it''s neurology. Building flexibility into intimacy rather than treating it as a fixed schedule respects these fluctuations.

**Transitions**: Moving between sensory states requires time. Going from a loud social event directly to intimate contact may be impossible without a sensory decompression period first.

