- **Avoidant attachment**: Values independence to the point of discomfort with closeness. May withdraw when partners need emotional connection.
- **Disorganized attachment**: Simultaneously craves and fears closeness. Often results from trauma at the hands of caregivers. The person they need for safety is also the source of danger.

