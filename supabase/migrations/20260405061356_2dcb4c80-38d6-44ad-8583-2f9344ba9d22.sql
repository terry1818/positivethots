Neurodivergent and neurotypical people often have fundamentally different communication styles. Neither is wrong — but misunderstandings between them are one of the most common sources of conflict in mixed-neurotype relationships. Learning to bridge these differences transforms frustration into deeper connection.

### The Double Empathy Problem

Researcher Damian Milton''s "double empathy problem" challenges the outdated assumption that autistic people lack empathy. The reality is that **communication breakdowns between autistic and neurotypical people are bidirectional** — both sides struggle to understand the other. Autistic people communicate effectively with other autistic people, and neurotypical people communicate effectively with other neurotypical people. The difficulty arises at the intersection.

This reframe is crucial: it moves from "the neurodivergent person has a deficit" to "we have different communication operating systems and need a translation layer."

### Common Communication Differences

**Direct vs. indirect communication**: Many neurodivergent people communicate directly and literally. They say what they mean and expect others to do the same. Neurotypical communication often relies on implication, subtext, and social softening ("I''m fine" meaning "I''m not fine but I don''t want to discuss it"). These different styles create constant misreadings.

**Processing speed**: ADHD can cause people to interrupt — not from rudeness, but because thoughts are fleeting and will be lost if not expressed immediately. Autistic people may need longer processing time before responding. Neither tempo is wrong, but partners need to understand and accommodate the difference.

**Emotional expression**: Neurodivergent people may express emotions differently. Flat affect (appearing unemotional) doesn''t mean absence of feeling — it may mean the internal experience isn''t mapping to expected facial expressions. Conversely, ADHD emotional dysregulation can produce reactions that seem disproportionate.

**Nonverbal communication**: Eye contact, facial expressions, body language, and tone of voice carry enormous meaning in neurotypical communication. Neurodivergent people may not produce or read these signals in expected ways, leading to misinterpretations ("You seem disinterested" when they''re actually fully engaged).

**Information sharing patterns**: Autistic people may share detailed information about special interests (info-dumping) as a form of connection and love. ADHD tangential thinking may make conversations jump between topics rapidly. Both patterns are expressions of engagement, not selfishness.

### Bridging Strategies

**Explicit communication agreements**: "In our relationship, we say what we mean. If something is bothering you, please tell me directly rather than expecting me to read signals." Making implicit rules explicit levels the playing field.

**Check-in questions**: "What did you hear me say?" and "What did you mean by that?" catch misunderstandings before they escalate. These feel awkward at first but become natural with practice.

**Written communication**: Many neurodivergent people communicate more effectively in writing, where they have time to process and compose without real-time pressure. Using text or email for important conversations isn''t avoidance — it might be accommodation.

**Scheduled conversations**: Rather than springing important discussions on a neurodivergent partner (especially one with ADHD), schedule them in advance. "Can we talk about our vacation plans tomorrow evening?" gives time to prepare mentally and emotionally.

**The "intent vs. impact" framework**: When communication goes wrong, separate intent from impact. "I know you didn''t intend to hurt me, but the impact of what you said was…" This prevents both defensive spiraling and self-blame.

### In Polyamorous Contexts

Navigating multiple communication styles across multiple relationships adds complexity. A neurodivergent person might communicate perfectly well with one partner but struggle with another — not because of the relationship quality, but because of neurotype compatibility. This isn''t favoritism; it''s communication style alignment.

Metamour communication can also be affected. Group planning, shared calendars, and polycule-wide decisions may need to accommodate different processing speeds, communication preferences, and sensory needs.

