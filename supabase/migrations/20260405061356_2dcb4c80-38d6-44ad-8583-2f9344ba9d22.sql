Trauma is not what happened to you — it''s what happens inside you as a result of what happened to you. This distinction, articulated by trauma researcher Dr. Gabor Maté, is fundamental to understanding how trauma shapes our relationships and our capacity for intimacy.

### What Trauma Is

Trauma occurs when an experience overwhelms our capacity to cope, leaving lasting changes in how we perceive ourselves, others, and the world. Trauma can result from:

- **Acute events**: A single overwhelming experience — assault, accident, natural disaster, sudden loss
- **Chronic exposure**: Ongoing harmful conditions — childhood neglect, domestic violence, systemic oppression, poverty
- **Developmental trauma**: Attachment disruptions during critical periods of brain development (the first seven years of life)
- **Betrayal trauma**: When the source of harm is someone you depend on for safety — a parent, partner, or trusted authority figure
- **Collective trauma**: Shared traumatic experiences affecting communities — racism, homophobia, transphobia, colonization, pandemic

**Important**: Trauma is defined by its impact, not by external judgment of its severity. What traumatizes one person may not traumatize another, and minimizing someone''s experience because "it wasn''t that bad" is itself harmful.

### Trauma and the Nervous System

Trauma fundamentally alters the nervous system''s stress response. Dr. Stephen Porges'' Polyvagal Theory identifies three states:

1. **Ventral vagal (safe and social)**: The calm, connected state where intimacy is possible. You feel grounded, present, and able to engage.
2. **Sympathetic activation (fight or flight)**: The mobilized state. Heart racing, muscles tensed, ready to defend or flee. In relationships, this looks like anger, anxiety, defensiveness, or the urge to leave.
3. **Dorsal vagal (freeze/collapse)**: The shutdown state. Numbness, dissociation, emotional flatness, feeling "checked out." In relationships, this looks like withdrawal, emotional unavailability, or going blank during conflict.

Traumatized individuals often have a nervous system that defaults to sympathetic or dorsal states — perceiving threat where there is none, or shutting down when closeness is offered. **This is not a choice. It''s a neurobiological adaptation that once served a protective purpose.**

