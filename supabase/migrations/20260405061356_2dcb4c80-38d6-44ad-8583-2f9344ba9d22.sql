- Accept "no," "slow down," and "I changed my mind" without any negative response
- Recognize that silence is not consent — especially for trauma survivors who may freeze rather than speak
- Understand that consent given under emotional duress (guilt, obligation, fear of abandonment) is not meaningful consent

**Normalize nervous system states**: Learn to name what''s happening without judgment.
- "I notice my chest is tight — I think my nervous system is activated. Can we slow down?"
- "I''m going into freeze right now. I need a few minutes with my feet on the floor."
- "I''m feeling the urge to people-please. Can you ask me what I actually want?"

### Co-Regulation Techniques

Co-regulation is the process of one person''s regulated nervous system helping to regulate another''s. Techniques include:

- **Synchronized breathing**: Breathe together slowly — in for 4, out for 6. The exhale-dominant ratio activates the parasympathetic nervous system.
- **Grounding touch**: Firm, steady pressure (holding hands, a hand on the back) with permission. Avoid light, unpredictable touch during activation.
- **Vocal regulation**: Slow, low, steady vocal tone. The vagus nerve responds to prosody (the musical quality of speech).
- **Physical proximity without demand**: Simply being near someone in a calm state, without requiring them to talk, engage, or perform okayness.

### The Limits of Partner-Provided Safety

While partners play a crucial role in trauma healing, there are important limits:

- **A partner cannot be your therapist**: They can support your healing, but they cannot guide the therapeutic process. Professional trauma therapy (EMDR, Somatic Experiencing, IFS, CPT) is essential.
- **A partner cannot always be regulated**: They have their own triggers, bad days, and limitations. Building multiple sources of co-regulation (friends, community, therapists, pets, nature) prevents over-reliance on one person.
- **Safety is not the same as comfort**: Growth sometimes requires discomfort. A trauma-informed relationship doesn''t avoid all difficult conversations — it creates conditions where difficult conversations can happen without overwhelming the nervous system.

